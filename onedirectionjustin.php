                Desde a época dos Beatles e Rolling Stones é assim. O lançamento simultâneo de bandas "inimigas" continua a alimentar a rivalidade: quem é o melhor? E em plena sexta-feira 13, quem entra na disputa são dois dos maiores exemplos de histeria juvenil dos últimos anos, Justin Bieber e One Direction. Cada qual a fim de provar que é melhor e mais relevante. Ao saber que o One Direction lançaria o novo disco no mesmo dia que ele, Bieber cutucou os rivais e postou um vídeo em que faz uma cara de espanto e justifica: "[Essa é] A cara que você faz quando você lança um álbum no mesmo dia de Justin Bieber". Em resposta, Louis Tomlinson imitou a careta ao falar sobre o show que o 1D fez na maior casa de espetáculos da Inglaterra. As alfinetadas são puro jogo de cena. Bieber inclusive revelou para uma rádio americana que Niall Horan é um de seus melhores amigos e que também estava ansioso pelo lançamento do novo trabalho dos britânicos. As fãs, no entanto, levam a disputa a sério. Com o vazamento dos discos nesta semana, ambas torcidas pediram para que os novos trabalhos fossem ouvidos apenas no dia do lançamento. E não deixaram de zoar as "inimigas". Afinal, o que está em jogo é: quem tem a maior base de fãs? E qual dos dois vai conquistar o primeiro lugar nas paradas? Após uma série de polêmicas que envolveram nudez, prisão e até má conduta no palco, Bieber lança o aguardado "Purpose" com a promessa de amadurecimento, embora o comportamento do cantor ainda continue o de um adolescente mimado e rebelde. Diante das amostras divulgadas, Bieber parece levar a melhor. Inspirado pela batida de DJs como Skrillex e pela marra do hip-hop, o cantor já apresentou a dançante "Sorry" e a batida eletrônica sutil e hipnótica de "What do You Mean?". "Love Yourself" é uma balada acompanhada apenas de guitarra. Uma evolução clara. Do outro lado, a última das boy bands a se tornar um fenômeno tenta provar que ainda tem muito gás para queimar com o disco "Made in the AM", mesmo que a saída de Zayn Malik e o anúncio de férias indeterminadas apontem o oposto. O 1D traz o mesmo som fofo, com mais consistência do que os hits anteriores. Prova disso é a boa "What a Felling" e a inspiração folk em "History". Já "End of the Day" tem gosto de mais do mesmo. As fãs enxergaram na letra da balada "Perfect" uma indireta de Harry Styles à ex-namorada Taylor Swift.
                
